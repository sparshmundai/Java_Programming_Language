
public class OneDArray 
{
    public static void main(String[] args) {
        int[] myarray = new int[4]; 
        
          myarray[0]=12;
          myarray[1]=45;
          myarray[2]=78;
          myarray[3]=65;
         
        
         System.out.println("My array "+myarray);
     
         // read all elements
       for(int i=0;i<myarray.length;i++)
       {
           System.out.println("MY ARRAY["+i+"]"+myarray[i]);
                   
       }
        
  
        
        
        
        
        
         
         
    
    
    }
    
    
    
    
    
    
}
